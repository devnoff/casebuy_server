개인정보 기록 열람 및 보존방법

(1) 거래가 있는 사아버몰에서 열람, 확인 가능케하고, 전자문서 형태로 정보처리스템등에 저장가능하도록 할것.

(2) 소비자가 희망하는 경우, 방문 또는 열람, 복사할 수 있도록 할것.
당사는 회원이 좋은정보를 안전하게 이용할 수 있도록 최선을 다하고 있습니다. 개인정보를 보호 하는데 있어 회원에게 고지한 사항들에 반하는 사고가 발생할 시, 그로 인해 발생한 손해에 대하여 개인정보 관리책임자가 책임을 집니다. 그러나 기술적인 보완조치를 했음에도 불구하고, 해킹 등 기본적인 네트워크상의 위험성에 의해 발생하는 예기치 못한 사고로 인한 정보의 훼손 및 방문자가 작성한 게시물에 의한 각종 분쟁에 관해서 당사는 책임이 없습니다. 회원의 개인정보를 취급하는 개인정보관리 책임자는 다음과 같습니다.

